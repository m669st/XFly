function pick(): Strings {
  const wanted = navigator.languages?.length ? navigator.languages : [navigator.language || 'en']
  for (const tag of wanted) {
    const lang = String(tag).split('-')[0].toLowerCase()
    if (TABLES[lang]) return TABLES[lang]
  }
  return en
}


export const t: Strings = pick()


export function fmt(template: string, values: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, k) => String(values[k] ?? `{${k}}`))
}
